package application;

public class Practicas {
	
	String num_convenio;
	String dni_alum;
	String fecha_inicio;
	String fecha_terminacion;
	String fecha_final;
	String dias_semana;
	String tipo_horario;
	String horas_al_dia;
	String total_horas;
	String hora_inicio;
	String hora_terminacion;
	
	
	public Practicas(String num_convenio, String dni_alum, String fecha_inicio, String fecha_terminacion,
			String fecha_final, String dias_semana, String tipo_horario, String horas_al_dia, String total_horas,
			String hora_inicio, String hora_terminacion) {
		super();
		this.num_convenio = num_convenio;
		this.dni_alum = dni_alum;
		this.fecha_inicio = fecha_inicio;
		this.fecha_terminacion = fecha_terminacion;
		this.fecha_final = fecha_final;
		this.dias_semana = dias_semana;
		this.tipo_horario = tipo_horario;
		this.horas_al_dia = horas_al_dia;
		this.total_horas = total_horas;
		this.hora_inicio = hora_inicio;
		this.hora_terminacion = hora_terminacion;
	}


	public String getNum_convenio() {
		return num_convenio;
	}


	public void setNum_convenio(String num_convenio) {
		this.num_convenio = num_convenio;
	}


	public String getDni_alum() {
		return dni_alum;
	}


	public void setDni_alum(String dni_alum) {
		this.dni_alum = dni_alum;
	}


	public String getFecha_inicio() {
		return fecha_inicio;
	}


	public void setFecha_inicio(String fecha_inicio) {
		this.fecha_inicio = fecha_inicio;
	}


	public String getFecha_terminacion() {
		return fecha_terminacion;
	}


	public void setFecha_terminacion(String fecha_terminacion) {
		this.fecha_terminacion = fecha_terminacion;
	}


	public String getFecha_final() {
		return fecha_final;
	}


	public void setFecha_final(String fecha_final) {
		this.fecha_final = fecha_final;
	}


	public String getDias_semana() {
		return dias_semana;
	}


	public void setDias_semana(String dias_semana) {
		this.dias_semana = dias_semana;
	}


	public String getTipo_horario() {
		return tipo_horario;
	}


	public void setTipo_horario(String tipo_horario) {
		this.tipo_horario = tipo_horario;
	}


	public String getHoras_al_dia() {
		return horas_al_dia;
	}


	public void setHoras_al_dia(String horas_al_dia) {
		this.horas_al_dia = horas_al_dia;
	}


	public String getTotal_horas() {
		return total_horas;
	}


	public void setTotal_horas(String total_horas) {
		this.total_horas = total_horas;
	}


	public String getHora_inicio() {
		return hora_inicio;
	}


	public void setHora_inicio(String hora_inicio) {
		this.hora_inicio = hora_inicio;
	}


	public String getHora_terminacion() {
		return hora_terminacion;
	}


	public void setHora_terminacion(String hora_terminacion) {
		this.hora_terminacion = hora_terminacion;
	}
	
	
	
	

}
